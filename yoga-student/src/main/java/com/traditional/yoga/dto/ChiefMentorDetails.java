package com.traditional.yoga.dto;

public class ChiefMentorDetails {

	private int chiefMentorId;
	private String chiefMentorName;
	private String chiefMentorNumber;

	public int getChiefMentorId() {
		return chiefMentorId;
	}

	public void setChiefMentorId(int chiefMentorId) {
		this.chiefMentorId = chiefMentorId;
	}

	public String getChiefMentorName() {
		return chiefMentorName;
	}

	public void setChiefMentorName(String chiefMentorName) {
		this.chiefMentorName = chiefMentorName;
	}

	public String getChiefMentorNumber() {
		return chiefMentorNumber;
	}

	public void setChiefMentorNumber(String chiefMentorNumber) {
		this.chiefMentorNumber = chiefMentorNumber;
	}

}
